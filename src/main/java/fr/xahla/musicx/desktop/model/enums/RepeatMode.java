package fr.xahla.musicx.desktop.model.enums;

public enum RepeatMode {

    /**
     * Do not repeat when the queue has no song left.
     */
    NO_REPEAT,

    /**
     * Repeat the entire queue when the queue has no song left.
     */
    QUEUE_REPEAT,

    /**
     * Repeat the current song.
     */
    SONG_REPEAT

}
